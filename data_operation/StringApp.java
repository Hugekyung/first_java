
public class StringApp {

	public static void main(String[] args) {

		System.out.println("Hello World!!"); // 글자들의 모음, String
		System.out.println('H'); // 한 글자만 표현, 웬만하면 쓰지 마라, Character
		
		System.out.println("Hello "
				+ "World");
		
		System.out.println("Hello \nWorld"); // \n: new line(줄바꿈)
		System.out.println("Hello \"World\""); // \문자: escape 문자열

	}

}
